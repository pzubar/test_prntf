#include "libft.h"

int		ft_strleni(char *str, int i)
{
	int count;

	count = 0;
	if (!str)
		return (0);
	while (str[i] != '\t' && str[i] != ' ' && str[i] != '\n' && str[i])
	{
		count++;
		i++;
	}
	return (count);
}
