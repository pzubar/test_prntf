#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int			i;
	size_t		num;

	num = n;
	i = 0;
	if (n != 0)
	{
		while (n != 0)
		{
			((unsigned char*)s)[i] = 0;
			i++;
			n--;
		}
	}
}
